<h2>FAQ</h2>

	<div id="accordion">
	 	<h5>Wat is beschermingsbewind?</h5>
	  		<div>
	    		<p>
	    			Bewindvoering, ook wel beschermingsbewind genoemd, is een wettelijke maatregel die bedoeld is voor meerderjarige personen die door hun lichamelijke of geestelijke toestand tijdelijk of blijvend niet (meer) in staat zijn om hun financiële belangen te behartigen.
	    		</p>
	  		</div>

	  	<h5>Wanneer is beschermingsbewind mogelijk?</h5>
	  		<div>
	    		<p>
	    			Volgens de wet is bewindvoering bedoeld voor mensen, die vanwege hun lichamelijke of geestelijke toestand niet in staat zijn om hun eigen financiën te regelen. Voor sommige mensen is bewindvoering tijdelijk, maar over het algemeen is het voor onbepaalde tijd. De kantonrechter is degene die beoordeelt of bewindvoering noodzakelijk is.
	    		</p>
	  		</div>

	  	<h5>Wat doet een bewindvoerder?</h5>
	  		<div>
	    		<p>
	    			Een bewindvoerder is bedoeld om u op financieel gebied te beschermen tegen anderen, maar ook tegen uzelf. Een bewindvoerder regelt al uw financiële zaken. De bewindvoerder mag namens u een handtekening zetten onder financiële stukken. Dit gebeurt zoveel mogelijk in overleg met u. Maar de bewindvoerder neemt in uw belang ook zelf initiatief en grijpt in als dat nodig is.
	    		</p>
	  		</div>

	  	<h5>Wat wordt onder bewind gesteld?</h5>
	  		<div>
	    		<p>
	    			Alle goederen van een meerderjarige kunnen onder bewind worden gesteld. Onder goederen wordt verstaan: banksaldo, loon, pensioen, uitkering, maar ook meubilair, sieraden, onroerende zaken, effecten, vorderingen en schulden. De kantonrechter kan, afhankelijk van de situatie, besluiten om slechts een gedeelte van de goederen onder bewind te stellen, bijvoorbeeld alleen het spaartegoed of onroerend goed.
	    		</p>
	  		</div>

	  	<h5>Wat verandert er voor u bij onder bewindstelling?</h5>
	  		<div>
	    		<p>
	    			Op het moment dat Encaasa tot bewindvoerder is benoemd, gaan er voor u een aantal dingen veranderen. Dit betekent dat u niet meer als enige over uw financiën kan beslissen. Zo zullen wij een brief sturen naar de instanties, bedrijven en eventuele schuldeisers waarmee u een financiële relatie heeft. In deze brief zullen we uitleggen dat uw vermogen onder bewind is gesteld. Om een bewindvoerder aan te stellen is een uitspraak van de rechtbank nodig. Iemand die onder bewind gesteld wordt, kan niet meer zelfstandig over zijn of haar vermogen of inkomsten beschikken, maar blijft wel handelingsbekwaam.
	    		</p>
	  		</div>

	  	<h5>Wie kunnen beschermingsbewind aanvragen?</h5>
	  		<div>
	    		<p>
	    			U kunt zelf bewindvoering aanvragen. Ook uw partner of familieleden, tot in de vierde graad, kunnen hiervoor een verzoek doen. Dat zijn ouders, grootouders, kinderen en kleinkinderen, broers, zusters, neven, nichten en ook ooms en tantes. Ook Encaasa kan het namens u aanvragen.
	    		</p>
	  		</div>

	  	<h5>Wat moet de kantonrechter weten?</h5>
	  		<div>
	    		<p>
	    			Als u de aanvraag bij Encaasa in behandeling geeft, wordt het verzoek aan de kantonrechter door Encaasa opgesteld. De kantonrechter vraagt om een medische verklaring van een arts, of in sommige situaties een sociaal verslag van een maatschappelijk werker. Aan de hand van de medische verklaring kan de kantonrechter beoordelen, of de situatie om over te gaan tot bewindvoering ook daadwerkelijk van belang is. U ondertekent de aanvraag zelf. Kunt u dit, om welke reden dan ook, niet ondertekenen, dan mag een familielid de aanvraag ook ondertekenen.
	    		</p>
	  		</div>

	  	<h5>Aan wie moet het verzoek worden gericht?</h5>
	  		<div>
	    		<p>
	    			Een verzoek tot onder bewindstelling moet worden gericht aan de kantonrechter, in de gemeente waar de persoon woont, voor wie de onderbewindstelling wordt aangevraagd.
	    		</p>
	  		</div>

	  	<h5>Wanneer treedt de bewindvoering officieel in werking?</h5>
	  		<div>
	    		<p>
	    			De bewindvoering treedt in werking wanneer de kantonrechter daartoe heeft besloten, en dat officieel op de dag, nadat de griffier van het kantongerecht u op de hoogte heeft gesteld van deze beslissing. Deze beslissing staat in een brief, die ook wel beschikking wordt genoemd. Wanneer de kantonrechter Encaasa officieel tot bewindvoerder heeft benoemd, moeten er een aantal zaken worden geregeld.
	    		</p>
	  		</div>

	  	<h5>Wie controleert de bewindvoerder?</h5>
	  		<div>
	    		<p>
	    			Jaarlijks legt de bewindvoerder aan de Rechtbank verantwoording af door middel van een overzicht van uw inkomsten en uitgaven in dat jaar.
	    		</p>
	  		</div>

	  	<h5>Wie betaalt de kosten voor bewindvoering?</h5>
	  		<div>
	    		<p>
	    			U betaalt zelf maandelijks voor de kosten van bewindvoering. Soms is het mogelijk de kosten via de bijzondere bijstand vergoed te krijgen.
	    		</p>
	  		</div>

	  	<h5>Wat is het verschil tussen bewindvoering (beschermingsbewind) en budgetbeheer?</h5>
	  		<div>
	    		<p>
	    			Beschermingsbewind wordt via de kantonrechter verkregen. Budgetbeheer wordt niet via de kantonrechter verkregen, maar er wordt gewerkt op basis van vrijwilligheid via een contract en een volmacht.
	    		</p>
	  		</div>

	  	<h5>Wanneer eindigt bewindvoering?</h5>
	  		<div>
	    		<p>
	    			Bewindvoering eindigt bij overlijden. Eerder kan het eindigen wanneer de bewindvoering wordt vervangen door curatele, of wanneer er een verzoekschrift tot opheffing van de bewindvoering aan de kantonrechter wordt gericht, en door de kantonrechter wordt gehonoreerd. In dit geval moet het wel aannemelijk zijn, dat men zelf weer volledig in staat is, om de financiën te regelen. Encaasa behoudt zich het recht voor de rechtbank te verzoeken de bewindvoering per direct te staken indien de cliënt zich niet conformeert aan de procedure. Bij beëindiging van bewindvoering moet Encaasa veelal een aantal extra werkzaamheden uitvoeren, zoals het informeren van instanties, het versturen van adreswijzigingen, het opmaken van een eindrekening en verantwoording, en eventueel het inschakelen van een notaris. De kosten voor deze extra werkzaamheden zijn voor betrokken cliënt.
	    		</p>
	  		</div>

